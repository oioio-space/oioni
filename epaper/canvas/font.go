// LoadTTF parses a TrueType font at the given size and DPI and returns
// a Font that rasterizes glyphs on demand.
// Requires golang.org/x/image/font/opentype.
func LoadTTF(data []byte, sizePt float64, dpi float64) (Font, error) {
	f, err := opentype.Parse(data)
	if err != nil {
		return nil, err
	}
	face, err := opentype.NewFace(f, &opentype.FaceOptions{
		Size: sizePt,
		DPI:  dpi,
	})
	if err != nil {
		return nil, err
	}
	metrics := face.Metrics()
	height := metrics.Height.Ceil()
	return &ttfFont{face: face, height: height}, nil
}

type ttfFont struct {
	face   xfont.Face
	height int
}

func (f *ttfFont) LineHeight() int { return f.height }

func (f *ttfFont) Glyph(r rune) (data []byte, width, height int) {
	// Get advance width
	advance, ok := f.face.GlyphAdvance(r)
	if !ok {
		return nil, 0, 0
	}
	w := advance.Ceil()
	h := f.height
	if w <= 0 || h <= 0 {
		return nil, 0, 0
	}

	// Rasterize to a gray image
	img := image.NewGray(image.Rect(0, 0, w, h))
	drawer := &xfont.Drawer{
		Dst:  img,
		Src:  image.White,
		Face: f.face,
		Dot:  fixed.Point26_6{X: 0, Y: f.face.Metrics().Ascent},
	}
	drawer.DrawString(string(r))

	// Threshold to 1-bit, pack MSB-first
	stride := (w + 7) / 8
	packed := make([]byte, stride*h)
	for row := 0; row < h; row++ {
		for col := 0; col < w; col++ {
			if img.GrayAt(col, row).Y >= 128 {
				// white pixel = 1 bit
				byteIdx := row*stride + col/8
				bitIdx := uint(7 - col%8)
				packed[byteIdx] |= 1 << bitIdx
			}
		}
	}
	return packed, w, h
}

// Font is the interface for 1-bit bitmap fonts used with DrawText.
//
// Glyph data format: row-major, 1-bit-per-pixel packed bytes.
//
//	byte index = (row * ceil(width/8)) + (col/8)
//	bit  index = 7 - (col % 8)   (MSB = leftmost pixel)
type Font interface {
	// Glyph returns the bitmap for rune r.
	// Returns nil data if the rune is not in the font.
	Glyph(r rune) (data []byte, width, height int)
	LineHeight() int
}

// EmbeddedFont returns a built-in bitmap font for the given size in points.
// Available sizes: 8, 12, 16, 20, 24.
// Returns nil for unsupported sizes.
func EmbeddedFont(sizePt int) Font {
	switch sizePt {
	case 8:
		return &bitmapFont{data: font8Data, glyphW: 5, glyphH: 8, firstRune: 0x20, lastRune: 0x7E}
	case 12:
		return &bitmapFont{data: font12Data, glyphW: 7, glyphH: 12, firstRune: 0x20, lastRune: 0x7E}
	case 16:
		return &bitmapFont{data: font16Data, glyphW: 8, glyphH: 16, firstRune: 0x20, lastRune: 0x7E}
	case 20:
		return &bitmapFont{data: font20Data, glyphW: 8, glyphH: 20, firstRune: 0x20, lastRune: 0x7E}
	case 24:
		return &bitmapFont{data: font24Data, glyphW: 8, glyphH: 24, firstRune: 0x20, lastRune: 0x7E}
	}
	return nil
}

// bitmapFont implements Font using packed row-major glyph data.
type bitmapFont struct {
	data      []byte
	glyphW    int
	glyphH    int
	firstRune rune
	lastRune  rune
}

func (f *bitmapFont) bytesPerGlyph() int {
	return ((f.glyphW + 7) / 8) * f.glyphH
}

func (f *bitmapFont) Glyph(r rune) (data []byte, width, height int) {
	if r < f.firstRune || r > f.lastRune {
		return nil, 0, 0
	}
	bpg := f.bytesPerGlyph()
	idx := int(r-f.firstRune) * bpg
	return f.data[idx : idx+bpg], f.glyphW, f.glyphH
}

func (f *bitmapFont) LineHeight() int { return f.glyphH }

